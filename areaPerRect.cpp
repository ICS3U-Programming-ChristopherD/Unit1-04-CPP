// This program calculates the area and perimeter of a rectangle.
//     with dimensions 8cm x 4cm

#include <iostream>

int main() {
    std::cout << "If a rectangle has the dimensions" << std::endl;
    std::cout << "8cm x 4cm" << std::endl;
    std::cout << "" << std::endl;
    std::cout << "The area is: " << (8 * 4) << "cm^2" << std::endl;
    std::cout << "The perimeter is: " << 2 * (8 + 4) << "cm" << std::endl;
}
